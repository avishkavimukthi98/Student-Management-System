    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
       String regNo = (String)jComboBox1.getSelectedItem();
       int i =0;
       ResultSet rs;
       
       try
       {
          Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/student","root","");
            PreparedStatement ps = con.prepareStatement("select firstName, lastName, dateofBirth, gender, address, email, mobilePhone, homePhone, parentName, nic, contactNo from registration where regNo=?");
            ps.setString(1, regNo);
            
            rs = (ResultSet) (Resultset) ps.executeQuery();
            
            while(rs.next())
            {
                fnametxt.setText(rs.getString("firstName"));
                lnametxt.setText(rs.getString("lastName"));
                calendar.setDate(rs.getDate("dateofBirth"));
                String gender = rs.getString("gender");
                {
                    if(gender.equals("female")){
                        maleradio.setSelected(false);
                        femaleradio.setSelected(false);
                    }else{
                        femaleradio.setSelected(false);
                        maleradio.setSelected(true);
                    }
                }
                addresstxtarea.setText(rs.getString("address"));
                emailtxt.setText(rs.getString("email"));
                mobiletxt.setText(rs.getString("mobilePhone"));
                hometxt.setText(rs.getString("homePhone"));
                parenttxt.setText(rs.getString("parentName"));
                nictxt.setText(rs.getString("nic"));
                contacttxt.setText(rs.getString("contactNo"));
                        
                    }
                }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null, e);
            }
       
    }//GEN-LAST:event_jComboBox1ItemStateChanged

